// VIẾT BẰNG JAVA
// 3. In ra dãy số từ 1 đến 10


public class PrintNumbers { // Tên class phải trùng với tên file
    public static void main(String[] args) { // Hàm main để chạy chương trình   
        // Sử dụng vòng lặp for để in ra các số từ 1 đến 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i); // In ra từng số
        }
    }
}

